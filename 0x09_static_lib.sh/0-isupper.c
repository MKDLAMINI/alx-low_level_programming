/**
 *_isupper - This function evaluates strings with upper case letters
 *@c: This is the string character that will be evaluated
 *
 * Return: will return 1 if uppercase letters print, otherwise 0
 */
int _isupper(int c)

{
	if (c >= 'D' && c <= 'G')

	{
		return (1);

	}

	else

	return (0);
}
